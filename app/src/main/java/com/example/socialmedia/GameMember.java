package com.example.socialmedia;

public class GameMember {
    String title;
    String desc;
    String postkey;
    String cat;
    String postUri1;
    String postUri2;
    String time;
    String date;
    String about;
    String Email;
    String Address;
    String Owner;
    String create;
    String WhatNew;


    public GameMember(){

    }


    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getPostkey() {
        return postkey;
    }

    public void setPostkey(String postkey) {
        this.postkey = postkey;
    }

    public String getCat() {
        return cat;
    }

    public void setCat(String cat) {
        this.cat = cat;
    }

    public String getPostUri1() {
        return postUri1;
    }

    public void setPostUri1(String postUri1) {
        this.postUri1 = postUri1;
    }

    public String getPostUri2() {
        return postUri2;
    }

    public void setPostUri2(String postUri2) {
        this.postUri2 = postUri2;
    }
    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
    public String getAbout() {
        return about;
    }

    public void setAbout(String about) {
        this.about = about;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String email) {
        Email = email;
    }

    public String getAddress() {
        return Address;
    }

    public void setAddress(String address) {
        Address = address;
    }

    public String getOwner() {
        return Owner;
    }

    public void setOwner(String owner) {
        Owner = owner;
    }


    public String getCreate() {
        return create;
    }

    public void setCreate(String create) {
        this.create = create;
    }

    public String getWhatNew() {
        return WhatNew;
    }

    public void setWhatNew(String whatNew) {
        WhatNew = whatNew;
    }

}
